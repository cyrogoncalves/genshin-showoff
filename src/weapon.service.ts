const calculateStats = (weapon: Weapon): BuildStats => {
  const weaponModel = WEAPONS.find(w => w.name === weapon.model.name);
  const stats = {};
  stats["ATK"] = scalingStats
      [weaponModel.rarity-1]
      .find(s => s[0][0] === weaponModel.baseAtk)
      [weapon.ascension]
      [ascensionLevelMap[weapon.ascension].findIndex(l => l === weapon.level)];
  stats[weaponModel.substat.type] = scalingSubstats[weaponModel.substat.base][Math.floor(weapon.level / 5)];
  return stats;
}

export const WeaponService = {
  calculateStats
}
